// Infinite loop
// Open System Monitor to view
// Ctrl + Z to kill process
#include <stdio.h>

int main()
{
  printf("Open SYSTEM MONITOR to view this process.\n");
  printf("Press Ctrl + C to kill this process.\n");
  printf("Press Ctrl + Z to pause this process.\n");
  while (1)
    ;
  return 0;
}
